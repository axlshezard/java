public class Asignacion {
    
    public static void operadores(){
        //  = es el simbolo que asigna un valor
        int a = 1;
        System.out.println("a =  " + a);
        /* += incrementamos el valor de la derecha al valor de la izquirda y lo 
        asignamos al valor de la izquierda */
        a+=2;
        System.out.println("a +=  " + a);
        // lo mismo pero restamos 
        a-=5;
        System.out.println("a -=  " + a);
        // multiplicamos
        a*=5;
        System.out.println("a *=  " + a);
        a/=5;
        System.out.println("a /=  " + a);
        a%=5;
        System.out.println("a %=  " + a);



    }
}
